package bean;

public class Query {
	private Road init;
	private Road end;
	private int initPlot;
	private int endPlot;

	public Road getInit() {
		return init;
	}

	public void setInit(Road init) {
		this.init = init;
	}

	public Road getEnd() {
		return end;
	}

	public void setEnd(Road end) {
		this.end = end;
	}

	public int getInitPlot() {
		return initPlot;
	}

	public void setInitPlot(int initPlot) {
		this.initPlot = initPlot;
	}

	public int getEndPlot() {
		return endPlot;
	}

	public void setEndPlot(int endPlot) {
		this.endPlot = endPlot;
	}
}
